using System.Reflection;
using System.Web;
using System.Web.Mvc;
using System.Web.Optimization;
using System.Web.Routing;
using Abp.Localization;
using Abp.Localization.Sources.Xml;
using Abp.Modules;
using DataModel.Data.ApplicationLayer.Services;
using DataModel.Data.DataLayer;

namespace PcdWeb.Web
{
    [DependsOn(typeof(PcdWebDataModule), typeof(PcdApplicationModule), typeof(PcdWebWebApiModule))]
    public class PcdWebWebModule : AbpModule
    {
        public override void PreInitialize()
        {
            //Add/remove languages for your application
            Configuration.Localization.Languages.Add(new LanguageInfo("en", "English", "famfamfam-flag-england", true));
            Configuration.Localization.Languages.Add(new LanguageInfo("tr", "Türkçe", "famfamfam-flag-tr"));

            //Add/remove localization sources here
            Configuration.Localization.Sources.Add(
                new XmlLocalizationSource(
                    PcdWebConsts.WebLocalizationSourceName,
                    HttpContext.Current.Server.MapPath("~/Localization/PcdWeb")
                    )
                );

            //Configure navigation/menu
            Configuration.Navigation.Providers.Add<PcdWebNavigationProvider>();
        }

        public override void Initialize()
        {
            IocManager.RegisterAssemblyByConvention(Assembly.GetExecutingAssembly());

            AreaRegistration.RegisterAllAreas();
            RouteConfig.RegisterRoutes(RouteTable.Routes);
            BundleConfig.RegisterBundles(BundleTable.Bundles);
        }
    }
}
